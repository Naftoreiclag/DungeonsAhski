	int loadedLevelId = 0;
	int headerSize = 2;
	
	public boolean placeFree(int x, int y)
	{
		return levels[loadedLevelId][y].charAt(x) != '[';
	}
	
	public void loadLevel(int levelId, HandlerEntity entHand, Entity myPlayer)
	{
		loadedLevelId = levelId;
		
		//entHand.killAll(0);//decor
		//entHand.killAll(2);//enemies
		//entHand.killAll(3);//portals
		
		entHand.killAllButPlayer(myPlayer);
		
		//entHand.killAll();
		
		//spawn entities and relocate player
		for(int i = headerSize;i < levels[loadedLevelId].length;i++)
		{
			for(int j = 0;j < levels[loadedLevelId][i].length();j++)
			{
				if(levels[loadedLevelId][i].charAt(j) == '+')
				{
					myPlayer.setPosition(j,i);
				}
				else if(levels[loadedLevelId][i].charAt(j) != '[' || levels[loadedLevelId][i].charAt(j) != '.')
				{
					//spawn entites
					entHand.spawnEntity(levels[loadedLevelId][i].charAt(j), j, i);
				}
			}
		}
	}
	
	public void drawLevel(GraphicsArrayDraw gObject, int x, int y)
	{
		char bracketEqu;
		
		if(levels[loadedLevelId][0].equalsIgnoreCase("u"))
		{
			gObject.drawRectangleFilledChar(0, 0, 78, 24, charWall);
			bracketEqu = charWall;
		}
		else
		{
			gObject.drawRectangleFilledChar(0, 0, 78, 24, charTree);
			bracketEqu = charTree;
		}
		
		for(int i = headerSize;i < levels[loadedLevelId].length;i++)
		{
			//.replace('x', charWall)
			for(int j = 0;j < levels[loadedLevelId][i].length();j++)
			{
				if(levels[loadedLevelId][i].charAt(j) == '[')
				{
					//only draw walls
					gObject.drawChar(bracketEqu, x + j, y + i);
				}
				else// if(levels[loadedLevelId][i].charAt(j) == '.')
				{
					//only draw walls
					gObject.drawChar(' ', x + j, y + i);
				}
			}
		}
	}
	
	public String getLevelDescription()
	{
		return levels[loadedLevelId][1];
	}

	public int getLoadedLevel()
	{
		return loadedLevelId;
	}
